% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
x='+-×÷';
n=str2num(get(handles.edit1,'String'));
global Str
Str=Run(n);

%global inform;
inform=[];
if get(handles.radiobutton1,'value')
    for n=1:6:length(Str)
    temp=sprintf('%3d%3c%3d%3c%3d =',Str(n),x(Str(n+3)),Str(n+1),x(Str(n+4)),Str(n+2));
    inform=[inform;temp];
    end
elseif get(handles.radiobutton2,'value')
    for n=1:6:length(Str)
    temp=sprintf('%3d%3c%3d%3c%3d = %3d',Str(n),x(Str(n+3)),Str(n+1),x(Str(n+4)),Str(n+2),Str(n+5));
    inform=[inform;temp];
    end
end
set(handles.listbox1,'String',inform);




